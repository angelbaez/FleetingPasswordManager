#include <QFile>
#include <QDate>
#include <QTextStream>
#include <QDomDocument>
#include <QDomElement>

#include "loginio.h"

bool LoginIO::importLogins(LoginIO::LoginList & rLogins, QString fileName)
{
    QDomDocument doc;

    QFile file(fileName);
    if (!file.open(QIODevice::ReadOnly))
    {
        return false;
    }

    if (!doc.setContent(&file))
    {
        file.close();
        return false;
    }

    file.close();

    QDomElement root = doc.documentElement();
    QDomNode    node = root.firstChild();

    rLogins.clear();

    while(!node.isNull())
    {
        QDomElement tag = node.toElement();
        if(!tag.isNull())
        {
            if (tag.nodeName() == "login")
            {
                QString url  = tag.attribute("url", "");
                QString user = tag.attribute("user", "");

                rLogins << QPair<QString, QString>(url, user);
            }

            node = node.nextSibling();
        }
    }

    return true;
}

bool LoginIO::exportLogins(LoginIO::LoginList logins, QString fileName)
{
    // Create content
    QDomDocument doc;
    QDomComment header = doc.createComment(QString("Generated by ") + NAME + " on " +
                                           QDate::currentDate().toString());
    doc.appendChild(header);

    QDomElement root = doc.createElement("logins");
    root.setAttribute("version", VERSION);
    doc.appendChild(root);

    for (int i = 0; i < logins.count(); i++)
    {
        QDomElement loginTag = doc.createElement("login");
        loginTag.setAttribute("url", logins.at(i).first);
        loginTag.setAttribute("user", logins.at(i).second);
        root.appendChild(loginTag);
    }

    QFile file(fileName);
    if (file.open(QIODevice::WriteOnly | QIODevice::Text))
    {
        QTextStream out(&file);
        out << doc.toString();
        file.close();
        return true;
    }

    return false;
}
